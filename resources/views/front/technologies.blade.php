                                    When molecules in the mixture are very similar, direct quantification becomes
                                    difficult. HPLC is a form of column chromatography used frequently to separate,
                                    identify and quantify compounds. It consists of a stationary phase that absorbs the
                                    analytes and holds them for a particular time. All the molecules in the mixture will
                                    spend different time with different stationary phase and then come out at the end.
                                    The time that a molecule takes to travel from one end to other end of stationary
                                    phase is known as Retention time. Hence all the molecules displays their own unique
                                    retention time for particular stationary-mobile phase combination. Silicagel is most
                                    frequently used as the adsorbent which is packed in a column. HPLC columns are
                                    densely packed as packing directly affects the resolution. At the end of the column,
                                    a detector is kept. As the molecules come out from the end, the detector detects and
                                    gives a peak on chromatogram. The peak area calculation quantifies the molecule of
                                    interest on the basis of standard values
