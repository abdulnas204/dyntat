                                    of scattered light. Of these three, size and shape determines the angle of scatter
                                    and concentration intensifies the scatter at particular angle. Generally, molecules
                                    vary in size and shape, thus they become unique from these aspects. As nephelometry
                                    is a quantitative method, on the basis of the scattering of known concentrations, an
                                    unknown concentration may be derived. The concept of nephelometry is mostly applied
                                    for estimations of proteins as the latest breakthrough.
