                                    LC-MS (or HPLC-MS, Liquid Chromatography - Mass Spectrometry) is the combination of
                                    the physico-chemical separation capabilities of liquid chromatography. Generally it
                                    is used for the detection and identification of chemicals in the presence of other
                                    chemicals (in a complex mixture or matrix).
                                    <br><br>
                                    The principle of LC-MS/MS is based on the fragmentation of charged ions and the
                                    detection of the resulting fragments. The interface is a particle beam type, which
                                    separates the sample from the solvent, and allows the introduction of the sample in
                                    the form of dry particles into the high vacuum region. Electron impact is of
                                    interest for molecules which do not ionize with API technique, or when an electron
                                    impact spectrum is necessary, since it provides spectral information independent of
                                    the sample introduction technique (GC or LC, or direct introduction) and instrument
                                    supplier. Thus it has a much higher selectivity and sensitivity than LC-MS and makes
                                    it possible to elucidate e.g. metabolite structures.
                                </p>
