                                    ELISA is an immunological technique which is used to detect the presence of an
                                    antibody or an antigen in a sample. An unknown amount of antigen is coated on a
                                    solid phase, and a specific antibody is washed over the surface so that it can bind
                                    to the antigen. The immune complex thus formed is linked to an enzyme, which acts on
                                    a substrate to give a coloured reaction which is detected using ultramodern ELISA
                                    plate readers. As in ELISA, the label that is added to produce detectable signal is
                                    an enzyme, therefore it is called as Enzyme Linked Immunosorbent Assay.
