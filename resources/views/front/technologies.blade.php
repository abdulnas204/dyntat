                                    Nephelometry is a quantitative method, based on the principle that a dilute
                                    suspension of small particles will scatter light (usually a laser) passed through it
                                    rather than simply absorbing it. The amount of scatter is determined by collecting
                                    the light at an angle.
                                    <br><br>
