                                    ICP-MS is the most advance technology used for doing trace metal analysis in
                                    biological and non biological samples.ICP-MS combination of two well established
                                    technology namely, ICP(Inductively coupled Plasma) source and mass spectrometry. ICP
                                    is a very high temperature excitation source ,the temperature which is maintained is
                                    about 10000 K,that effectively vaporizes,excites and ionizes the atoms. The Mass
                                    spectrometry is a technology which discriminates these ions depending on their mass
                                    to charge ratio. Mass spectrometry is used for quantification of atoms and
                                    molecule,thus ICP-MS gives quantitative as well as qualitative results.
                                    <br><br>
                                    Applications apart from blood specimen analysis, it is also used in elemental
                                    analysis in food industries, drinking and waste water industries.
