                                <p>
                                    Photometry is the science of measuring visible light and is based on a relationship
                                    between absorption of light and the properties of the material through which the
                                    light is traveling (Beer Lambert's law).
                                    <span class="pic w_frame alignright">
                                    <img src="{{ asset('public/assets/images/Ins_Olympus_AU_2700.jpg') }}" alt="PHOTOMETRY">
                                </span>
                                    <br><br>
                                    Whenever light of a particular wavelength enters a solution of a substance, it comes
                                    out with a reduced intensity, this is because a part of it is absorbed by the
                                    solution. If this property needs to be exploited for the analytical work or
                                    biochemical assays, the phenomenon of absorption of light should obey the
                                    Beer-Lambert's Law. It can be stated as that the intensity of light decreases
                                    exponentially with the increase in the concentration of the solution and the depth
                                    or thickness of the solution through which the light passes.
                                    <br><br>
                                    This technology is integrated into various instruments which enables the detection
                                    of analytes.
